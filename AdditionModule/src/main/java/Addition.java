public class Addition {
    private int number1;
    private int number2;

    public void setNum1(int number)
    {
        number1=number;
    }

    public void setNum2(int number)
    {
        number2 = number;
    }


    public int getNum1() {
        return number1;
    }

    public int getNum2() {
        return number2;
    }

    public int addition()
    {
        return number1 + number2;
    }




}
